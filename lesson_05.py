# Lesson 5: Function Arguments

# TODO 1: Positional arguments:
# - Write a function `add(a, b)` that returns the sum of two numbers.
# - Call the function with positional arguments and print the result.

# TODO 2: Keyword arguments:
# - Write a function `greet(name, age)` that prints "Hello, [name], you are [age] years old."
# - Call the function using keyword arguments.

# TODO 3: Default arguments:
# - Modify `greet` to have a default value of `age=30`.
# - Call the function with and without providing `age`.

# TODO 4: Variable-length arguments (*args):
# - Write a function `sum_all(*args)` that returns the sum of all arguments.
# - Call the function with varying numbers of arguments.

# TODO 5: Keyword-only arguments:
# - Write a function `describe_person(name, *, age, city)` that prints a formatted description.
# - Call the function with keyword arguments.

# TODO 6: Mixing positional and keyword arguments:
# - Write a function `calculate(a, b, operation="add")`:
#   - If `operation="add"`, return `a + b`.
#   - If `operation="multiply"`, return `a * b`.
# - Call the function using both positional and keyword arguments.

# TODO 7: Using **kwargs:
# - Write a function `show_details(**kwargs)` that prints key-value pairs.
# - Call the function with arbitrary keyword arguments.

# TODO 8: Nested function:
# - Write a function `outer_function(x)`:
#   - Inside it, define `inner_function(y)` that returns `x + y`.
#   - Return the result of `inner_function` for `y=10`.

# TODO 9: Function annotations:
# - Write a function `subtract(a: int, b: int) -> int` that returns `a - b`.
# - Print the annotations of the function.

# TODO 10: Debugging function arguments:
# - Write a function with argument mismatches.
# - Use print statements to debug and fix the issues.
